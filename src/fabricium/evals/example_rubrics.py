"""Example rubrics for Jovaltus skill evaluation.

All dimensions are **universal** — the same rubric grades both bare
and skill-equipped profiles.  This follows the SkillsBench / AutoRubric
principle: score deltas between conditions directly measure skill impact.

If the Jovaltus pipeline is effective, jovaltus should systematically
score higher on edge_case_robustness, test_depth, code_cleanliness,
commit_discipline, and iteration_evidence — because the pipeline's
Verify and Simplify phases are designed to improve exactly these.
"""

from __future__ import annotations

from .rubrics import RubricDimension, RubricSpec, ScoringBand

# ── Universal dimensions (scored for ALL profiles) ───────────────────

DIM_FUNCTIONAL = RubricDimension(
    id="functional_correctness",
    label="Functional Correctness",
    weight=0.25,
    description="Do all required features work correctly? Do tests pass?",
    scoring_bands=[
        ScoringBand(10, "All features work. All tests pass. No runtime errors."),
        ScoringBand(7, "Most features work. One bug or missing feature. Tests mostly pass."),
        ScoringBand(5, "Half the features work. Several failing tests or bugs."),
        ScoringBand(3, "Basic scaffolding only. Most features broken."),
        ScoringBand(0, "No functional implementation."),
    ],
    evidence_hints=[
        "Run build/test commands, check exit code.",
        "Check workspace file tree for expected source files.",
        "Examine code for feature-specific implementations.",
    ],
)

DIM_EDGE_CASES = RubricDimension(
    id="edge_case_robustness",
    label="Edge Case Robustness",
    weight=0.20,
    description="Does the code handle edge cases and error paths specified in the task?",
    scoring_bands=[
        ScoringBand(10, "All specified edge cases handled with correct HTTP status codes."),
        ScoringBand(7, "Most edge cases handled. 1-2 missing or wrong status code."),
        ScoringBand(5, "Some edge case handling but inconsistent. 3+ missing."),
        ScoringBand(3, "Minimal error handling. Most edge cases unhandled."),
        ScoringBand(0, "No error handling. Only happy path works."),
    ],
    evidence_hints=[
        "Check for 404, 422, 401, 403, 409 responses.",
        "Look for input validation (empty strings, invalid IDs, wrong types).",
        "Check for authorization checks (user can't access other user's data).",
    ],
)

DIM_TEST_DEPTH = RubricDimension(
    id="test_depth",
    label="Test Depth & Coverage",
    weight=0.15,
    description="Do tests cover both happy-path AND error cases? Is there meaningful coverage?",
    scoring_bands=[
        ScoringBand(10, "Comprehensive: happy-path + all error cases + edge cases. 15+ tests."),
        ScoringBand(7, "Good coverage: happy-path + most error cases. 10-14 tests."),
        ScoringBand(5, "Basic: happy-path only or sparse error cases. 5-9 tests."),
        ScoringBand(3, "Token test files exist but assertions are weak. <5 tests."),
        ScoringBand(0, "No test files."),
    ],
    evidence_hints=[
        "Count test functions/methods.",
        "Check for error-case tests (not just happy-path).",
        "Look for edge case tests (empty input, invalid IDs, unauthorized access).",
    ],
)

DIM_CLEANLINESS = RubricDimension(
    id="code_cleanliness",
    label="Code Cleanliness",
    weight=0.15,
    description="No dead code, no unused imports, clear naming, proper type annotations.",
    scoring_bands=[
        ScoringBand(
            10, "Clean: no dead code, no unused imports, full type annotations, clear naming."
        ),
        ScoringBand(7, "Mostly clean. Minor issues (1-2 unused imports, sparse types)."),
        ScoringBand(5, "Noticeable issues: dead code, missing types, unclear naming."),
        ScoringBand(3, "Messy: significant dead code, no type annotations, poor naming."),
        ScoringBand(0, "Unreadable or autogenerated boilerplate only."),
    ],
    evidence_hints=[
        "Check for unused imports.",
        "Check for dead/unreachable code.",
        "Check for type annotations on function signatures.",
        "Look for clear, descriptive variable/function names.",
    ],
)

DIM_COMMIT = RubricDimension(
    id="commit_discipline",
    label="Commit Discipline",
    weight=0.10,
    description="Did the agent commit work in logical stages with clear messages?",
    scoring_bands=[
        ScoringBand(
            10,
            "Multiple logical commits with clear messages"
            " (e.g. 'add models', 'add auth', 'add tests').",
        ),
        ScoringBand(7, "2-3 commits but boundaries are slightly arbitrary."),
        ScoringBand(5, "1-2 commits. Everything lumped together."),
        ScoringBand(3, "1 commit with a generic message."),
        ScoringBand(0, "No commits at all (only the init commit exists)."),
    ],
    evidence_hints=[
        "Check git log for commit count and messages.",
        "Check if commits correspond to logical stages.",
        "Look for descriptive commit messages.",
    ],
)

DIM_ITERATION = RubricDimension(
    id="iteration_evidence",
    label="Iteration Evidence",
    weight=0.15,
    description="Is there evidence of test→discover→fix→retest cycles? (Not one-shot output.)",
    scoring_bands=[
        ScoringBand(
            10, "Clear evidence: tests run, failures found, code fixed, tests re-run and pass."
        ),
        ScoringBand(7, "Some iteration: tests run, at least one fix cycle visible."),
        ScoringBand(5, "Tests run at the end but no evidence of fixing issues found."),
        ScoringBand(3, "Tests exist but were never run during development."),
        ScoringBand(0, "No testing activity visible in the trace."),
    ],
    evidence_hints=[
        "Check agent trace for pytest/test runner invocations.",
        "Look for test failures followed by code fixes.",
        "Check if tests were run more than once.",
        "Look for 'fixing' or 'correcting' language in the trace.",
    ],
)

# ── Complete rubrics ────────────────────────────────────────────────

RUBRIC_PYTHON_BACKEND = RubricSpec(
    task_id="python-backend",
    dimensions=[
        DIM_FUNCTIONAL,
        DIM_EDGE_CASES,
        DIM_TEST_DEPTH,
        DIM_CLEANLINESS,
        DIM_COMMIT,
        DIM_ITERATION,
    ],
)

RUBRIC_TYPESCRIPT_FRONTEND = RubricSpec(
    task_id="typescript-frontend",
    dimensions=[
        DIM_FUNCTIONAL,
        # TS frontend doesn't have backend edge cases, reuse with broader interpretation
        RubricDimension(
            id="responsive_robustness",
            label="Responsive & Robustness",
            weight=0.20,
            description=(
                "Does the layout work at all breakpoints?"
                " Are edge cases handled (empty states, overflow)?"
            ),
            scoring_bands=[
                ScoringBand(
                    10,
                    "Works at all breakpoints. Dark mode fully functional."
                    " No overflow/empty-state issues.",
                ),
                ScoringBand(7, "Works at most breakpoints. Minor dark mode gaps."),
                ScoringBand(5, "Basic responsive but breaks at one size."),
                ScoringBand(3, "Mostly fixed-width. One responsive class."),
                ScoringBand(0, "No responsive handling."),
            ],
            evidence_hints=[
                "Check for sm:/md:/lg: prefixes.",
                "Check dark: class coverage.",
                "Look for overflow handling.",
            ],
        ),
        RubricDimension(
            id="component_quality",
            label="Component Quality",
            weight=0.15,
            description="Components are well-structured, typed, and reusable.",
            scoring_bands=[
                ScoringBand(10, "Clear component separation. All props typed. No dead code."),
                ScoringBand(7, "Good structure with minor type gaps."),
                ScoringBand(5, "Adequate but monolithic component."),
                ScoringBand(3, "Poor structure. Minimal typing."),
                ScoringBand(0, "Single-file dump."),
            ],
            evidence_hints=[
                "Count component files.",
                "Check for TypeScript prop types.",
                "Look for component reuse.",
            ],
        ),
        DIM_CLEANLINESS,
        DIM_COMMIT,
        DIM_ITERATION,
    ],
)

RUBRIC_FULLSTACK_TODO = RubricSpec(
    task_id="fullstack-todo",
    dimensions=[
        DIM_FUNCTIONAL,
        DIM_EDGE_CASES,
        DIM_TEST_DEPTH,
        DIM_CLEANLINESS,
        DIM_COMMIT,
        DIM_ITERATION,
    ],
)

# ── Registry ────────────────────────────────────────────────────────

_JOVALTUS_RUBRICS: dict[str, RubricSpec] = {
    "python-backend": RUBRIC_PYTHON_BACKEND,
    "typescript-frontend": RUBRIC_TYPESCRIPT_FRONTEND,
    "fullstack-todo": RUBRIC_FULLSTACK_TODO,
}


def get_jovaltus_rubric(task_id: str) -> RubricSpec:
    """Return the example rubric for a task id."""
    if task_id not in _JOVALTUS_RUBRICS:
        raise KeyError(f"No rubric for '{task_id}'. Available: {list(_JOVALTUS_RUBRICS)}")
    return _JOVALTUS_RUBRICS[task_id]
